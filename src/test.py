class BrainFrack:
    def __init__(self, x):
        self.code = x

    def Evaluate(code):
        array = [0]
        x = 0
        index = 0
        loop = []
        while x < len(code):
            if code[x] == ">":
                index += 1
                if index == len(array):
                    array.append(0)
            if code[x] == "<":
                index -= 1
                if index < 0:
                    array.insert(0, 0)
                    index += 1
            if code[x] == "+":
                array[index] += 1
                if array[index] == 256:
                    array[index] = 0

            if code[x] == "-":
                array[index] -= 1
                if array[index] == -1:
                    array[index] = 255

            if code[x] == ".":
                print(chr(array[index]),end="")

            if code[x] == ",":
                array[index] = ord(input(">>> "))

            if code[x] == "[":
                loop.append(x)

            if code[x] == "]":
                if array[index] != 0:
                    x = loop[-1]
                else:
                    del loop[-1]
            x += 1

